    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        TextArea.setText("");
        int index = Lista.getSelectedIndex();
        String linea = "";
        switch(index){
            case 0:{
                System.out.println("Inventario");
                linea = linea + TfMarca.getText();
                TextArea.append(linea);
                break;
            }
            case 1:{
                linea = linea + TfPeso.getText();
                System.out.println("Vehiculo");
                TextArea.append(linea);
                break;
            }
            case 2:{
                linea = linea + TfNumero.getText()+ "--" + TfPrecio.getText()+ "--" + TfCilindrada.getText()+
                "--" + TfCilindros.getText()+ "--" + TfDescripcion.getText() + "--" + TfColor.getText();
                System.out.println("Compontentes");
                TextArea.append(linea);
                break;
            }
    }
       
    }//GEN-LAST:event_btnMostrarActionPerformed

