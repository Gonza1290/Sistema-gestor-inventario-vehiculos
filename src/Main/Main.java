    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
       int peso = Integer.parseInt(TfPeso.getText());
       int numero = Integer.parseInt(TfNumero.getText());
       float precio = Float.parseFloat(TfPrecio.getText());
       int cantCilindrada = Integer.parseInt(TfCilindrada.getText());
       int cilindros = Integer.parseInt(TfCilindros.getText());
       String marca = TfMarca.getText();
       String descripcion = TfDescripcion.getText();
       String color = TfColor.getText();
       
       Controlador.grabar(marca,peso,numero,precio,cantCilindrada,cilindros,descripcion,color);
       Inicializar();
       btnInventario.setVisible(true);
    }//GEN-LAST:event_btnGrabarActionPerformed

